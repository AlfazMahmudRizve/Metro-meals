"use client";

import { useEffect, useState } from "react";
import { createClient } from "@supabase/supabase-js";
import { motion, AnimatePresence } from "framer-motion";
import { ChefHat, X } from "lucide-react";
import { useRouter } from "next/navigation";

const supabase = createClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!
);

export default function OrderReadyToast({ customerId }: { customerId?: string }) {
    const [notification, setNotification] = useState<{ id: string, table?: string } | null>(null);
    const [audio] = useState(typeof Audio !== "undefined" ? new Audio("https://assets.mixkit.co/active_storage/sfx/2869/2869-preview.mp3") : null);
    const router = useRouter();

    useEffect(() => {
        if (!customerId) return;
        let channel: any;

        const setupListener = async () => {
            // Listen for UPDATE events on the orders table
            channel = supabase
                .channel("order_updates")
                .on(
                    "postgres_changes",
                    {
                        event: "UPDATE",
                        schema: "public",
                        table: "orders",
                        filter: `customer_id=eq.${customerId}`, // Filter for this customer's orders
                    },
                    (payload) => {
                        const newOrder = payload.new;
                        const oldOrder = payload.old;

                        // Check if status changed to completed
                        if (newOrder.status === "completed" && oldOrder.status !== "completed") {
                            setNotification({
                                id: newOrder.id,
                                table: newOrder.table_number
                            });

                            if (audio) {
                                try { audio.play(); } catch (e) { console.error("Audio play failed", e); }
                            }
                        }
                    }
                )
                .subscribe();
        };

        setupListener();

        return () => {
            if (channel) supabase.removeChannel(channel);
        };
    }, [audio]);

    if (!notification) return null;

    return (
        <AnimatePresence>
            <motion.div
                initial={{ opacity: 0, y: 50, scale: 0.9 }}
                animate={{ opacity: 1, y: 0, scale: 1 }}
                exit={{ opacity: 0, y: 20, scale: 0.9 }}
                className="fixed bottom-24 right-4 md:bottom-8 md:right-8 z-[10000] max-w-sm w-full"
            >
                <div className="bg-espresso text-cream p-5 rounded-2xl shadow-2xl border-2 border-latte flex items-start gap-4 reltive overflow-hidden">
                    {/* Glow Effect */}
                    <div className="absolute top-0 right-0 w-20 h-20 bg-green-500/20 blur-2xl rounded-full -mr-10 -mt-10" />

                    <div className="bg-green-500 p-3 rounded-full text-white animate-bounce">
                        <ChefHat size={24} />
                    </div>

                    <div className="flex-1">
                        <h4 className="font-heading font-bold text-lg leading-tight mb-1">Order Ready! üçΩÔ∏è</h4>
                        <p className="text-sm text-cream/80 font-sans mb-3">
                            {notification.table
                                ? `Your food for Table ${notification.table} is coming now!`
                                : "Your takeout/delivery order is ready!"}
                        </p>
                        <button
                            onClick={() => {
                                setNotification(null);
                                router.push(`/success?id=${notification.id}`);
                            }}
                            className="text-xs font-bold bg-cream text-espresso px-3 py-1.5 rounded-lg hover:bg-white transition-colors"
                        >
                            View Receipt
                        </button>
                    </div>

                    <button
                        onClick={() => setNotification(null)}
                        className="text-cream/50 hover:text-cream transition-colors"
                    >
                        <X size={18} />
                    </button>
                </div>
            </motion.div>
        </AnimatePresence>
    );
}
